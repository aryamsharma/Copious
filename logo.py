def logo1(beginwith="", endwith="\n"):
    """
    Hour-glass
    """
    print(f"{beginwith}+====+\n|(::)|\n| )( |\n|(..)|\n+====+", end=endwith)

def logo2(beginwith="", endwith="\n"):
    """
    Hour-glass with X to the right
    """

    print(f"{beginwith}+====+\n|(::)|  \-/\n| )( |   X\n|(..)|  /-\ \n+====+", end=endwith)


